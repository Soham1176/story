#include <iostream>
using namespace std;

int main()
{
    int a,b;
    cin>>a>>b;
    
    cout<<"The Value of a and b is: "<<a<<b<<endl;
    cout<<"The Value of a & b is: "<< (a&b) <<endl;
    cout<<"The Value of a | b is: "<< (a|b) <<endl;
    cout<<"The Value of a ~ b is: "<< (~a) <<endl;
    cout<<"The Value of a ^ b is: "<< (a^b) <<endl;
}




